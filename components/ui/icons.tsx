// Utility function to display formatted date with moment
export const FormattedDate: React.FC<{ date: string; format?: string }> = ({
  date,
  format = "MMMM D, YYYY",
}) => {
  return <>{moment(date).format(format)}</>;
};

// Relative time component using moment
export const TimeAgo: React.FC<{ date: string }> = ({ date }) => {
  return <>{moment(date).fromNow()}</>;
};
